# From your project root

# General
npm install --save-dev typescript eslint prettier babel-jest @babel/core

# React Native (core, if not already)
npm install react-native

# iOS (already done, but for reference)
sudo gem install cocoapods

# Android
# (No npm install needed, but you must install Android Studio and set up SDK/NDK)

# Web
npm install react react-dom react-native-web

# Desktop (Electron, optional)
npm install --save-dev electron

# Watchman (macOS only, for file watching)
brew install watchman

# If you use React Navigation, install its dependencies:
npm install @react-navigation/native
npm install react-native-screens react-native-safe-area-context

# If you use Redux or Zustand for state:
npm install redux react-redux
# or
npm install zustand

# If you use React Query:
npm install @tanstack/react-query

# If you use MUI (Material UI) for web:
npm install @mui/material @emotion/react @emotion/styled
